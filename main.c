FT_Error init_font(FT_Library* library, FT_Face* face) {
    FT_Error error = FT_Init_FreeType(library);
    if (error) {
        printf("Error: Unable to init freetype.");
        return error;
    }
    
    const char filename[] = "Font.ttf";
    
    error = FT_New_Face(*library,
                        filename,
                        0,
                        face);
    if (error == FT_Err_Unknown_File_Format) {
        printf("Error: Unsupported font file format.");
        return error;
    } else if (error) {
        printf("Error: Unable to read font file: '%s'.", filename);
        return error;
    }
    
    FT_UInt dpi = 384;
    
    error = FT_Set_Char_Size(*face,
                             0,
                             32*64,
                             dpi,
                             dpi);
    if (error) {
        printf("Error: Unable to set character size.");
        return error;
    }
    
    return 0;
}

FT_Error free_font(FT_Library* library, FT_Face* face) {
    FT_Error error = FT_Done_Face(*face);
    if (error) {
        printf("Error: Unable to free the face.");
        return error;
    }
    
    error = FT_Done_FreeType(*library);
    if (error) {
        printf("Error: Unable to free freetype library.");
        return error;
    }
    
    return 0;
}

int generate_glyph(FT_Face* face, FT_ULong charcode, int* out_bitmap_width, int* out_bitmap_rows, unsigned char** out_bitmap) {
    
    
    FT_UInt glyph_index = FT_Get_Char_Index(*face, charcode);
    
    FT_Int32 load_flags = 0;
    
    FT_Error error = FT_Load_Glyph(*face,
                                   glyph_index,
                                   load_flags);
    if (error) {
        printf("Error: Unable to load glyph.");
        return error;
    }
    
    FT_Render_Mode render_mode = FT_RENDER_MODE_MONO;
    
    error = FT_Render_Glyph((*face)->glyph,
                            render_mode);
    if (error) {
        printf("Error: Unable to render glyph.");
        return error;
    }
    
    FT_Bitmap* bitmap = &(*face)->glyph->bitmap;
    
    *out_bitmap_width = bitmap->width;
    *out_bitmap_rows = bitmap->rows;
    create_bitmap(bitmap->width, bitmap->rows, out_bitmap);
    // convert to our bitmap format
    int bits_per_row = 16 * (bitmap->width / 16 + (bitmap->width % 16 > 0 ? 1 : 0));
    for (int y = 0; y < bitmap->rows; ++y) {
        for (int x = 0; x < bitmap->width; ++x) {
            int offset = y * bits_per_row + x;
            int byte = offset / 8;
            int bit = 7 - (offset - byte * 8);
            int p = bitmap->buffer[byte] & (1 << bit);
            set_pixel(bitmap->width, bitmap->rows, *out_bitmap, x, y, p);
        }
    }
    
    return 0;
}

